package com.scivicslab.pojoactor.plugin;

import com.scivicslab.pojoactor.core.ActorProvider;
import com.scivicslab.pojoactor.core.ActorSystem;

/**
 * Sample ActorProvider implementation for testing ServiceLoader mechanism.
 *
 * This class demonstrates how plugins can automatically register actors
 * using the Java ServiceLoader pattern.
 *
 * @author devteam@scivics-lab.com
 * @version 2.0.0
 */
public class MathPluginProvider implements ActorProvider {

    @Override
    public void registerActors(ActorSystem system) {
        // Register MathPlugin actors
        system.actorOf("math1", new MathPlugin());
        system.actorOf("math2", new MathPlugin());
    }

    @Override
    public String getPluginName() {
        return "MathPlugin";
    }

    @Override
    public String getPluginVersion() {
        return "1.0.0";
    }
}
